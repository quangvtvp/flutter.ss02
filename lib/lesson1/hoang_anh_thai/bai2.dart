void main() {
  int number1 = 100;
  int number2 = 200;
  print('Tổng của $number1 và $number2 là ${number1 + number2}');
  print('Hiệu của $number1 và $number2 là ${number1 - number2}');
  print('Tích của $number1 và $number2 là ${number1 * number2}');
  print('Thương của $number1 và $number2 là ${number1 / number2}');
  return;
}
