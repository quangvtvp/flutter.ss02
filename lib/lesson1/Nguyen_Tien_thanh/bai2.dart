void main(){
  double number1 = 5.5;
  double number2 = 3.5;
  // Tính tổng, hiệu, tích, thương.
  double tong = number1 + number2 ;
  double hieu = number1 - number2 ;
  double tich = number1 * number2 ;
  double thuong = number1 / number2 ;
  print('Tổng : $tong');
  print('Hiệu : $hieu');
  print('tích : $tich');
  print('Thương ; $thuong');
}